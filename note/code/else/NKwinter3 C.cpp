/*
给定n,问能否用 4 5 6 凑出n,即 4x+5y+6z=n, x,y,z >= 0 ,如果能,找到 x,y,z,如果不能输出-1 


(1)直观的想法是完全背包 O(n*3) 完全可以解决问题 

(2)将 n 在 mod 4 意义下分成四类 考虑能不能全部覆盖
        1.  4k mod 4 = 0 能覆盖 所有 n mod 4 = 0 
        2. 4k+5 mod 4 = 1 能覆盖 n mod 4 = 1  (除1)
        3. 4k+6 mod 4 = 2 能覆盖 n mod 4 = 2  (除2)
        4.4k+5+6 mod 4 = 3 能覆盖 n mod 4 = 3 (除3 7) 
    结论就是除1,2,3,7外,都可以覆盖, 并且能O(1) 找到系数

*/