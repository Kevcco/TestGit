const int mod=998244353,N=5010,inf=0x3f3f3f3f;

int n,a[N];


void solve()
{
    scanf("%d",&n);
    int sum=0;
    for(int i=1;i<=n;i++) scanf("%d",a+i),sum+=a[i];
    if(!sum) printf("-1");
    else
    {
        for(int i=1;i<n;i++)
        {
            for(int j=1;j<=n;j++)
            {
                if(a[j]!=inf&&(sum-a[j]))
                {
                    sum-=a[j];a[j]=inf;
                    printf("%d ",j);
                    break;
                }
            }
        }
        for(int i=1;i<=n;i++)
            if(a[i]!=inf)
            {
                printf("%d",i);
                break;
            }
    }
    puts("");
    return;
}

